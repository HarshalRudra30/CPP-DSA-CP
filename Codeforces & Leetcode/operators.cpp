#include<iostream>
using namespace std;

int main()
{
    int a=4,b=5;
    cout<<"Comparing values of a and b :"<<endl;
    cout<<"a = b "<<(a==b)<<endl;
    cout<<"a > b "<<(a>b)<<endl;
    cout<<"a < b "<<(a<b)<<endl;
    cout<<"a >= b "<<(a>=b)<<endl;
    cout<<"a < b "<<(a<=b)<<endl;
    cout<<"a != b "<<(a!=b)<<endl;

    return 0;
}